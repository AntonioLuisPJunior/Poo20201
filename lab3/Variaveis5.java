package lab3;

public class Variaveis5 {
    public static void main(String[] args) {
        int valor = 5;
        System.out.println(++valor);// incrementa antes de printar
        System.out.println(valor);
        valor = 5;
        System.out.println(valor++);//printa e depois incrementa
        System.out.println(valor);
    }
    
}
